def mt19937_matrix():
    M = zero_matrix(GF(2), 19937, 19937)
    a = list(map(int,f'{0x9908B0DF:032b}'[::-1]))
    for i in range(19937 - 32):
        M[i, i+32] = 1
    for i in range(32):
        M[-32+i, 397*32-31+i] = 1
    for i in range(32-2):
        M[-32+i, i+2] = 1
    for i in range(32):
        M[-32+i, 1] = a[i]
    M[-2, 0] = 1
    return M

def vec32_to_state(v):
    return int(''.join(map(str, v))[::-1], 2)

hint = [int(i) for i in open("output.txt", "r").read().strip()]

# Recovering states
S    = Matrix(GF(2), 0, 19937, sparse=False)
T    = temper_matrix()
M    = mt19937_matrix()
M623 = M**623; print('compute M^623 done')
TMP  = M623*M; print('compute M^624 done')

cache_i = 623
for i in trange(1, len(hint)):
    while i > cache_i:
        TMP *= M623
        cache_i += 623

    mat_i = (i-1) % 623
    S = S.stack((T * TMP[1 + mat_i*32 : 1 + mat_i*32 + 32])[-1:])

v = vector(GF(2), hint[1:])
w = S.solve_right(v)

seed_states = [0x80000000]
for i in range(1, 624):
    seed_states.append(vec32_to_state(w[1 + i*32 - 32 : 1 + i*32]))

# Solve for state to get flag
solver = RandomSolver()
solver.init_seed_finder(19968)
z3_seed_states = solver.get_seed_states()
solver.solver_constrants.extend([z3_seed_state == seed_state for seed_state, z3_seed_state in zip(seed_states, z3_seed_states)])
solver.solve()

print(search(b'idek{[!-z]*}', long_to_bytes(solver.get_seed())))
